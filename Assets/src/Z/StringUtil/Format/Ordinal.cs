using System.Collections.Generic;

namespace Z.StringUtil.Format {
  public class Ordinal : IFormatter {
    public string format(string value, Dictionary<string, string> dictionary) {
      long number = long.Parse(value);
      long abs = System.Math.Abs(number);

      string suffix = null;

      if (abs >= 11 && abs <= 13) {
        suffix = "th";
      } else {
        switch (abs % 10) {
        case 1:
          suffix = "st";
          break;
        case 2:
          suffix = "nd";
          break;
        case 3:
          suffix = "rd";
          break;
        default:
          suffix = "th";
          break;
        }
      }

      if (suffix != null) {
        value = number.ToString() + suffix;
      }

      return value;
    }
  }
}
